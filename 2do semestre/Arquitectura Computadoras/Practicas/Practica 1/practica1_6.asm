ORG 3000H
MUL_A: MOV DX, 0  ; CH y CL valores de operandos. DX valor de retorno
FOR_A: CMP CH, 0
	JZ FIN_A
	ADD DL, CL
	ADC DH, 0
	DEC CH
	JMP FOR_A
FIN_A: RET

MUL_B: MOV DH, [BX]  ; BX y CX referencias a operandos. DX valor de retorno
	MOV BX, CX
	MOV CL, [BX]
	MOV CH, DH
	MOV DX, 0
FOR_B: CMP CH, 0
	JZ FIN_B
	ADD DL, CL
	ADC DH, 0
	DEC CH
	JMP FOR_B
FIN_B: RET

