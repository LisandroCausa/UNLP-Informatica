ORG 3000H
ES_VOCAL: CMP AH, 41H ; AH - PARAMETRO DE ENTRADA Y SALIDA
	JZ RET_TRUE
	CMP AH, 45H
	JZ RET_TRUE
	CMP AH, 49H
	JZ RET_TRUE
	CMP AH, 4FH
	JZ RET_TRUE
	CMP AH, 55H
	JZ RET_TRUE
	CMP AH, 61H
	JZ RET_TRUE
	CMP AH, 65H
	JZ RET_TRUE
	CMP AH, 69H
	JZ RET_TRUE
	CMP AH, 6FH
	JZ RET_TRUE
	CMP AH, 75H
	JZ RET_TRUE
	
	MOV AH, 00H
	RET
RET_TRUE: MOV AH, 0FFH
	RET

ORG 1000H
	CHAR DB "A"
ORG 2000H
	MOV AH, CHAR ; PARAMETRO
	CALL ES_VOCAL
END
