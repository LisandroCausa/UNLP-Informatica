INICIO EQU 65
FIN EQU 90
PIC EQU 20H
ORG 40
	RUT_DIR DW RUT_F10
ORG 1000H
	LETRA DB ?
ORG 3000H
RUT_F10: PUSH AX
	MOV CH, 0
	MOV LETRA, DL
	MOV AL, 20H
	OUT PIC, AL
	POP AX
	IRET
ORG 2000H
	CLI
	MOV AL, 10
	OUT PIC+4, AL
	MOV AL, 0FEH
	OUT PIC+1, AL
	MOV DL, INICIO
	MOV CH, 1
	STI
LOOP: CMP CH, 1
	JNZ FIN_LOOP
	INC DL
	CMP DL, FIN+1
	JNZ LOOP
	MOV DL, INICIO
	JMP LOOP
FIN_LOOP: MOV BX, OFFSET LETRA
	MOV AL, 1
	INT 7
	INT 0
END
