TIMER EQU 10H
PIC EQU 20H
PA EQU 30H
PB EQU 31H
CA EQU 32H
CB EQU 33H
ORG 120
	IP_CLK DW RUT_CLK
ORG 1000H
RUT_CLK: PUSH AX
	MOV AL, 0
	OUT TIMER, AL
	INC CL
	JNC FIN
	INT 0
FIN: MOV AL, 20H
	OUT PIC, AL
	POP AX
	IRET
ORG 2000H
	CLI
	MOV AL, 0FDH
	OUT PIC+1, AL
	MOV AL, 30
	OUT PIC+5, AL
	MOV AL, 1
	OUT TIMER+1, AL
	MOV AL, 0
	OUT TIMER, AL
	OUT CB, AL
	MOV AL, 0FFH
	OUT CA, AL
	STI
LOOP: IN AL, PA
	NOT AL
	AND AL, CL
	OUT PB, AL
	JMP LOOP
END
